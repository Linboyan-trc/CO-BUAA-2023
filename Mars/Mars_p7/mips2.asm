       	lui     $k1  0x8000
       	and     $t0  $k0 $k1
       	bne     $t0  $0  BD
       	nop
       	jal  BD_end
       	BD:
       	mfc0 $k1 $14
       	addi $k1 $k0 4
       	mtc0 $k1 $14
       	BD_end:
       	ori 	$k1, $0, 0x007c
       	and	$k0, $k1, $k0
       	beq 	$0, $k0, interrupt
       	nop
       	ori 	$k1, $0, 16
